/*EUROPA-----------------------*/
console.group("Europa")
console.log("Europa");

var absoluteEuropa: number = 4209.3;   /* Wert entnommen aus Tabelle: 2018->Europa */
var relativeEuropa: number = 100 / (31116.5 / 4209);  /* 100 / (Tabelle: alle Emissionswerte der Kontinente von 2018 / 2018->Europa)*/
var growthEuropa: number = (4209.3 - 4965.7) / 4965.7; /* (Wert von 2018->Europa - Wert von 2008->Europa) / Wert von 2008->Europa */
var growthabsoluteEuropa: number = 4209.3 - 4965.7;  /* Wert von 2018->Europa - Wert von 2008->Europa*/

console.log("Die Emission von Europa ist:" + absoluteEuropa + "kg CO2");
console.log("Relativ zur Gesamtemission der Welt verursacht Europa damit" + relativeEuropa +"%");
console.log("Für Europa hat sich 2018 im Vergleich zu 2008 die Emission um" + growthEuropa + "% verändert");
console.log("2018 im Vergleich zu 2008 sind das" + growthabsoluteEuropa + "kg CO2");
console.groupEnd("Europa");
